    private boolean isEmpty(){
        return tId.getText().equals("") || tTen.getText().equals("")
                || tGia.getText().equals("")
                || tNhaSX.getText().equals("")
                || tDungLuong.getText().equals("")
                || tMauSac.getText().equals("");
    }
    
    private  boolean  isValidatedData(){
        if(isEmpty()){
            JOptionPane.showMessageDialog(null, "Hay nhap du du lieu!");
            return false;
        }
        try{
            Double.parseDouble(tGia.getText());
            return true;
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Nhap gia dung!");
            return  false;
        }
    }
    
    private DienThoai getDienThoai(){
        String id = tId.getText();
        String name = tTen.getText();
        double gia = Double.parseDouble(tGia.getText());
        String nhaSx = tNhaSX.getText();
        String mauSac = tMauSac.getText();
        String dungLuong = tDungLuong.getText();
        
        return new DienThoai(nhaSx, dungLuong, mauSac, id, name, gia);
    }
