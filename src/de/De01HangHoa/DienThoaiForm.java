    private void bLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLuuActionPerformed
        // TODO add your handling code here:
        try{
            File f = new File("dienthoai.bat");
            FileWriter fw = new FileWriter(f);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(DienThoai x: list){
                bw.write(x.toString());
                bw.newLine();
                System.out.println(x.toString());
            }
            JOptionPane.showMessageDialog(null, "Nhap file thanh cong");
            bw.close();
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Khong thanh cong");
            Logger.getLogger(DienThoaiForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bLuuActionPerformed

